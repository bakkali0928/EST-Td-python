"""
variables i, max, min, rang_max, rang_min : Entiers
tableau T(100) : Reels
Début
max <-- T[0]
min <-- T[0]
rang_max <-- 0
rang_min <-- 0

Pour i de 0 à 99 Faire:
    si T[i] > max Alors:
       max <-- T[i]
       rang_max <-- i

    Finsi
    si T[i] < min Alors:
       min <-- T[i]
       rang_min <-- i+1
    Finsi
finPour
POUR CHAQUE élément DANS T FAIRE:
print("Le maximum des nombres est: ",max," Et leur indice est: ",rang_max,"Le minimum des nombres est: ",min," Et leur indice est: ",rang_min)
    
"""
T = []
for i in range(100):
    saisie = int(input("Saisie le "+str(i+1)+" nombre: "))
    T.append(saisie)
max = T[0]
min = T[0]
rang_max = 0
rang_min = 0
for i in range(100):
    if T[i] > max:
       max = T[i]
       rang_max = i+1
    if T[i] < min:
       min = T[i]
       rang_min = i+1
for element in T:
    print(element)
print("Le maximum des nombres est: ",max," Et leur indice est: ",rang_max,"Le minimum des nombres est: ",min," Et leur indice est: ",rang_min)




















































































































































































































































































































































































































































































