#Écrire un Algorithme qui affiche la somme des prix d'une suite d'articles en DH (entiers) saisies par l'utilisateur et se terminant par zéro
num_art = int(input("Entrer le nombre de votre articles: "))
som = 0
i = 0
while i < num_art:
    prix_art = int(input("Entrez le prix de l'article "+str(i+1)+"(termine par 0): "))
    if prix_art % 10 == 0:
        som = som + prix_art 
        i = i + 1
    else:
        print("Le nombre ne termine pas par 0")
print("La somme des prix est: ",som, " DH")
    


















































































































































































































































































































































































































































































